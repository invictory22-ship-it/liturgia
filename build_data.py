import json, re, sys
from collections import Counter

src = json.load(open('_blocks_current.json', encoding='utf-8'))

def norm(s):
    return re.sub(r'\s+', ' ', s).strip()

CORPUS = norm(' '.join(b['text'] for b in src))

def check(text, where=''):
    n = norm(text)
    if n and n not in CORPUS:
        print('!!! ФІДЕЛІТІ-ПОМИЛКА у', where)
        print('    фрагмент:', repr(n[:140]))
        sys.exit(1)

def find(sub):
    hits = [i for i, b in enumerate(src) if sub in b['text']]
    if len(hits) != 1:
        print('!!! find НЕ унікальний:', repr(sub[:60]), '->', len(hits), 'збігів')
        sys.exit(1)
    return hits[0]

def B(t, role, text):
    return {'t': t, 'role': role, 'text': text}

replacements = {}      # idx -> list[block]
ADDED = ''       # маркер доданого тексту (виключається з перевірки)

def replace(sub, newblocks):
    i = find(sub)
    for b in newblocks:
        if b['t'] in ('say', 'stage', 'choir', 'header') and not b.get('_added'):
            check(b['text'], sub[:40])
    replacements[i] = newblocks

def setrole(sub, role, t=None):
    i = find(sub)
    b = dict(src[i])
    b['role'] = role
    if t: b['t'] = t
    replacements[i] = [b]

def split(sub, *cuts):
    """Розрізати блок по підрядках-маркерах cuts=[(маркер, тип, роль), ...].
       Кожен cut починає новий шматок ВІД свого маркера."""
    i = find(sub)
    text = src[i]['text']
    # cuts: список (marker, t, role). Перший шматок — до першого маркера.
    points = []
    for marker, _, _ in cuts:
        pos = text.find(marker)
        if pos < 0:
            print('!!! split: маркер не знайдено:', repr(marker[:40]), 'у', repr(sub[:40]))
            sys.exit(1)
        points.append(pos)
    return i, text, points

