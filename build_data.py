# ---- ПЕРЕВІРКА ВІРНОСТІ: повний текст до = після ----
def texts_of(blocks):
    parts = []
    for b in blocks:
        if b.get('_added'):           # доданий підпис fold — пропускаємо
            if b['t'] == 'fold':
                parts.extend(c['text'] for c in b['blocks'])
            continue
        if b['t'] == 'fold':
            parts.extend(c['text'] for c in b['blocks'])
        else:
            parts.append(b['text'])
    return norm(' '.join(parts))

before = norm(' '.join(b['text'] for b in src))
after = texts_of(out)
if before != after:
    with open('_diff.txt', 'w', encoding='utf-8') as f:
        for k in range(min(len(before), len(after))):
            if before[k] != after[k]:
                f.write('РОЗБІЖНІСТЬ на позиції %d\n' % k)
                f.write('ДО   : ...%s...\n' % before[max(0,k-80):k+80])
                f.write('ПІСЛЯ: ...%s...\n' % after[max(0,k-80):k+80])
                break
        f.write('len before %d after %d\n' % (len(before), len(after)))
    sys.exit(1)

